// Helper functions
export const getBarsByCountry = (countryCode: string): KavaBar[] => {
  return kavaBars.filter(bar => bar.countryCode === countryCode);
};

export const getBarsByState = (state: string): KavaBar[] => {
  return kavaBars.filter(bar => bar.state.toLowerCase() === state.toLowerCase());
};

export const getBarsByRating = (minRating: number): KavaBar[] => {
  return kavaBars.filter(bar => bar.rating >= minRating);
};

export const searchBars = (query: string): KavaBar[] => {
  const lowerQuery = query.toLowerCase();
  return kavaBars.filter(bar => 
    bar.name.toLowerCase().includes(lowerQuery) ||
    bar.city.toLowerCase().includes(lowerQuery) ||
    bar.state.toLowerCase().includes(lowerQuery) ||
    bar.country.toLowerCase().includes(lowerQuery)
  );
};

export const getUniqueStates = (countryCode: string): string[] => {
  const states = kavaBars
    .filter(bar => bar.countryCode === countryCode)
    .map(bar => bar.state);
  return Array.from(new Set(states)).sort();
};
